package Models;

public class InfectionHistory {
    private String dateOfInfection;
    private String typeOfInfection;

    public String getDateOfInfection() {
        return dateOfInfection;
    }

    public void setDateOfInfection(String dateOfInfection) {
        this.dateOfInfection = dateOfInfection;
    }

    public String getTypeOfInfection() {
        return typeOfInfection;
    }

    public void setTypeOfInfection(String typeOfInfection) {
        this.typeOfInfection = typeOfInfection;
    }
}
